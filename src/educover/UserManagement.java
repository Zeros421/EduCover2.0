    private void loadLectureData(){ //Puts all the info in to the table 
        
        try {
            DefaultTableModel model = FillLectureTable.getLectureTableInformation(LectureFilePath);
            lectureTable.setModel(model);
            setLectureTableColumnWidths();
            setupStatusDropdown();
            addStatusChangeListener();
        }catch (IOException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error reading lecture data file.");
        }
        
        lectureTable.getModel().addTableModelListener(e -> {
    if (e.getType() == javax.swing.event.TableModelEvent.UPDATE) {

        int row = e.getFirstRow();
        int col = e.getColumn();

        if (col == 4) { // Status column
            DefaultTableModel model =
                    (DefaultTableModel) lectureTable.getModel();

            String userId = model.getValueAt(row, 0).toString();
            String newStatus = model.getValueAt(row, 4).toString();

            System.out.println(
                "Status changed -> User: " + userId + ", New Status: " + newStatus
            );

            // Later: updateStatusInFile(userId, newStatus);
        }
    }
});
        
    }
    
