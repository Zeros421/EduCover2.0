    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
    int selectedRow = lectureTable.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a user to delete");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this user?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) return;

    DefaultTableModel model = (DefaultTableModel) lectureTable.getModel();
    String userId = model.getValueAt(selectedRow, 0).toString();

    boolean success = RemoveUser.deleteUserFromFile(userId);

    if (success) {
        JOptionPane.showMessageDialog(this, "User deleted successfully");
        loadLectureData();
    } else {
        JOptionPane.showMessageDialog(this, "Failed to delete user");
    }
    }//GEN-LAST:event_DeleteActionPerformed

